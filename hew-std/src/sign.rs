    /// Helper: free a `BytesTriple` allocated by the runtime.
    ///
    /// # Safety
    ///
    /// `triple.ptr` must have been allocated by `hew_bytes_from_static` or
    /// another `alloc_buf`-based allocator.
    unsafe fn free_bytes_triple(triple: &BytesTriple) {
        if !triple.ptr.is_null() {
            // SAFETY: caller guarantees ptr was allocated by the runtime allocator.
            unsafe { hew_runtime::bytes::hew_bytes_drop(triple.ptr) };
        }
    }

    /// Helper: extract bytes from a `BytesTriple` into a `Vec`.
    fn triple_to_vec(triple: &BytesTriple) -> Vec<u8> {
        if triple.ptr.is_null() || triple.len == 0 {
            return Vec::new();
        }
        // SAFETY: triple.ptr + offset is valid for len bytes per BytesTriple invariant.
        unsafe {
            std::slice::from_raw_parts(triple.ptr.add(triple.offset as usize), triple.len as usize)
        }
        .to_vec()
    }

