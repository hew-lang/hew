/// Generate an Ed25519 PKCS#8 v2 document and return it as an 83-byte
/// `bytes` `HewVec`.
///
/// Calls `hew_panic_msg` on RNG failure (should not occur in practice).
///
/// # Safety
///
/// No inputs; all memory is managed by the runtime allocator.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_generate_pkcs8_hew() -> *mut hew_cabi::vec::HewVec {
    let mut buf = [0u8; ED25519_PKCS8_LEN];
    // SAFETY: buf is a valid ED25519_PKCS8_LEN-byte writable buffer.
    let ok = unsafe { hew_ed25519_generate_pkcs8(buf.as_mut_ptr()) };
    if ok == 0 {
        panic_msg(b"sign.keypair failed: Ed25519 key generation error (RNG failure)\0");
    }
    // SAFETY: buf is a valid byte slice.
    unsafe { hew_cabi::vec::u8_to_hwvec(&buf) }
}

/// Extract the 32-byte public key from an Ed25519 PKCS#8 `bytes` `HewVec`
/// and return it as a new 32-byte `bytes` `HewVec`.
///
/// Calls `hew_panic_msg` if `pkcs8` is null, has the wrong `HewVec` shape, or
/// is not a valid 83-byte PKCS#8 document.
///
/// # Safety
///
/// `pkcs8` must be a valid, non-null pointer to a `bytes` `HewVec`.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_public_key_from_pkcs8_hew(
    pkcs8: *mut hew_cabi::vec::HewVec,
) -> *mut hew_cabi::vec::HewVec {
    // Validate and copy bytes without panicking on null/malformed HewVec.
    // SAFETY: if pkcs8 is non-null the caller guarantees it points to a live HewVec.
    let Some(pkcs8_bytes) = (unsafe { hew_cabi::vec::try_hwvec_to_u8(pkcs8) }) else {
        panic_msg(
            b"sign.keypair failed: pkcs8 must be a valid bytes HewVec (non-null, plain i32 elements)\0",
        )
    };
    let mut pub_buf = [0u8; ED25519_PUBLIC_LEN];
    // SAFETY: pkcs8_bytes and pub_buf are valid buffers.
    let ok = unsafe {
        hew_ed25519_public_key_from_pkcs8(
            pkcs8_bytes.as_ptr(),
            pkcs8_bytes.len(),
            pub_buf.as_mut_ptr(),
        )
    };
    if ok == 0 {
        panic_msg(b"sign.keypair failed: private_key must be an 83-byte Ed25519 PKCS#8 document\0");
    }
    // SAFETY: pub_buf is a valid byte slice.
    unsafe { hew_cabi::vec::u8_to_hwvec(&pub_buf) }
}

/// Sign `message` (a `bytes` `HewVec`) with `private_key` (an 83-byte PKCS#8
/// `bytes` `HewVec`) and return the 64-byte signature as a `bytes` `HewVec`.
///
/// Calls `hew_panic_msg` if either input is null, has the wrong `HewVec`
/// shape, or `private_key` is not a valid 83-byte PKCS#8 document.
///
/// # Safety
///
/// - `private_key` must be a valid, non-null pointer to a `bytes` `HewVec`.
/// - `message` must be a valid, non-null pointer to a `bytes` `HewVec`.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_sign_hew(
    private_key: *mut hew_cabi::vec::HewVec,
    message: *mut hew_cabi::vec::HewVec,
) -> *mut hew_cabi::vec::HewVec {
    // Validate and copy bytes without panicking on null/malformed HewVec.
    // SAFETY: if private_key is non-null the caller guarantees it points to a live HewVec.
    let Some(key_bytes) = (unsafe { hew_cabi::vec::try_hwvec_to_u8(private_key) }) else {
        panic_msg(
            b"sign.sign failed: private_key must be a valid bytes HewVec (non-null, plain i32 elements)\0",
        )
    };
    // SAFETY: if message is non-null the caller guarantees it points to a live HewVec.
    let Some(msg_bytes) = (unsafe { hew_cabi::vec::try_hwvec_to_u8(message) }) else {
        panic_msg(
            b"sign.sign failed: message must be a valid bytes HewVec (non-null, plain i32 elements)\0",
        )
    };

    let mut sig_buf = [0u8; ED25519_SIG_LEN];
    // SAFETY: key_bytes and msg_bytes are valid slices; sig_buf is a 64-byte buffer.
    let ok = unsafe {
        hew_ed25519_sign(
            key_bytes.as_ptr(),
            key_bytes.len(),
            msg_bytes.as_ptr(),
            msg_bytes.len(),
            sig_buf.as_mut_ptr(),
        )
    };
    if ok == 0 {
        panic_msg(b"sign.sign failed: private_key must be an 83-byte Ed25519 PKCS#8 document\0");
    }
    // SAFETY: sig_buf is a valid 64-byte slice.
    unsafe { hew_cabi::vec::u8_to_hwvec(&sig_buf) }
}

/// Verify an Ed25519 `signature` over `message` using `public_key`.
///
/// Returns `1` if the signature is valid, `0` otherwise.  Never panics or
/// aborts â€” null/malformed `HewVec` inputs, wrong key/signature lengths, and
/// verification failures all return a clean `0`.
///
/// # Safety
///
/// - `public_key`, `message`, and `signature` must each be either null or a
///   valid pointer to a live `bytes` `HewVec`.  Dangling non-null pointers are
///   undefined behaviour.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_verify_hew(
    public_key: *mut hew_cabi::vec::HewVec,
    message: *mut hew_cabi::vec::HewVec,
    signature: *mut hew_cabi::vec::HewVec,
) -> i32 {
    // Validate each HewVec without panicking: null or malformed â†’ return 0.
    // SAFETY: if non-null the caller guarantees public_key is a live HewVec.
    let Some(key_bytes) = (unsafe { hew_cabi::vec::try_hwvec_to_u8(public_key) }) else {
        return 0;
    };
    // SAFETY: if non-null the caller guarantees message is a live HewVec.
    let Some(msg_bytes) = (unsafe { hew_cabi::vec::try_hwvec_to_u8(message) }) else {
        return 0;
    };
    // SAFETY: if non-null the caller guarantees signature is a live HewVec.
    let Some(sig_bytes) = (unsafe { hew_cabi::vec::try_hwvec_to_u8(signature) }) else {
        return 0;
    };

    // SAFETY: all slices are valid; raw verify handles length mismatches cleanly.
    unsafe {
        hew_ed25519_verify(
            key_bytes.as_ptr(),
            key_bytes.len(),
            msg_bytes.as_ptr(),
            msg_bytes.len(),
            sig_bytes.as_ptr(),
            sig_bytes.len(),
        )
    }
}

