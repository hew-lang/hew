/// Panic with a static NUL-terminated message via the Hew runtime.
///
/// Diverges: `hew_panic_msg` does not return.
fn panic_msg(message: &'static [u8]) -> ! {
    use std::ffi::c_char;
    // SAFETY: message is a valid static NUL-terminated string.
    unsafe { hew_runtime::actor::hew_panic_msg(message.as_ptr().cast::<c_char>()) };
    unreachable!("hew_panic_msg should not return")
}

/// Abort the process with no fabricated crypto output.
fn abort_sign_failure() -> ! {
    // SAFETY: abort terminates fail-closed; no fabricated crypto bytes escape.
    unsafe { libc::abort() }
}

/// Allocate a `BytesTriple` from a fixed-size byte slice.
fn bytes_from_slice(bytes: &[u8]) -> BytesTriple {
    let len = u32::try_from(bytes.len()).unwrap_or_else(|_| abort_sign_failure());
    // SAFETY: `bytes` is valid for `len` bytes; `hew_bytes_from_static` copies it
    // into a fresh, refcount-1 bytes allocation owned by the Hew caller.
    unsafe { hew_runtime::bytes::hew_bytes_from_static(bytes.as_ptr(), len) }
}

/// Extract a `Vec<u8>` from a by-pointer `BytesTriple`.
///
/// Returns `None` if the pointer is null, treating that as an empty/absent input.
/// A null pointer with zero len returns `Some(Vec::new())`.
///
/// # Safety
///
/// `triple` must be either null or a valid pointer to a `BytesTriple` whose
/// `ptr + offset` region is valid for `len` bytes.
unsafe fn bytes_from_triple(triple: *const BytesTriple) -> Option<Vec<u8>> {
    if triple.is_null() {
        return None;
    }
    // SAFETY: caller guarantees triple is non-null and valid.
    let t = unsafe { &*triple };
    if t.len == 0 {
        return Some(Vec::new());
    }
    if t.ptr.is_null() {
        return None;
    }
    let offset = t.offset as usize;
    let len = t.len as usize;
    // SAFETY: triple.ptr + offset is valid for len bytes per BytesTriple invariant.
    Some(unsafe { std::slice::from_raw_parts(t.ptr.add(offset), len) }.to_vec())
}

/// Copy `src` into the fixed-size buffer `out` only when `src.len() == N`.
///
/// This is the single checked seam in front of every fixed-buffer
/// `copy_nonoverlapping` in this module.  `out` is a caller-owned buffer valid
/// for exactly `N` bytes; the length equality is the guard that keeps a copy
/// from running past it if ring's encoded length ever diverges from the
/// documented constant.
///
/// Returns `1` and writes all `N` bytes on a length match; returns `0` and
/// writes **nothing** on a mismatch.  The check is an ordinary `if` so it holds
/// in every build profile â€” a `debug_assert!` here would be stripped in release
/// and reintroduce the overflow risk this seam exists to remove.
///
/// # Safety
///
/// `out` must be valid for writing `N` bytes.  `src` must be a valid slice.
unsafe fn fill_fixed<const N: usize>(out: *mut u8, src: &[u8]) -> i32 {
    // Fail closed: refuse to write when the source length does not match the
    // fixed buffer.  Not a release-stripped `debug_assert!` â€” this is the guard
    // that prevents a fixed-buffer overflow in all profiles.
    if src.len() != N {
        return 0;
    }
    // SAFETY: out is valid for N bytes per caller contract, and src.len() is
    // verified to equal N above.
    unsafe { std::ptr::copy_nonoverlapping(src.as_ptr(), out, N) };
    1
}

