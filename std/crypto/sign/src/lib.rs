//! Hew runtime: `sign` module.
//!
//! Provides Ed25519 key-pair generation, signing, and verification for
//! compiled Hew programs.  All key and signature material is carried as
//! raw byte buffers; there are no opaque handle types.
//!
//! ## Size constants (Ed25519 / ring 0.17)
//!
//! | Constant              | Bytes | Source                                      |
//! |-----------------------|-------|---------------------------------------------|
//! | `ED25519_PKCS8_LEN`   |   83  | `ring` PKCS#8 v2 document (private + public)|
//! | `ED25519_PUBLIC_LEN`  |   32  | raw 32-byte Curve25519 public point         |
//! | `ED25519_SIG_LEN`     |   64  | raw 64-byte signature (r â€– s)               |
//!
//! ## Ownership / ABI contract (for the reviewer)
//!
//! Every `_hew`-suffixed function follows the same ownership contract as the
//! sibling `std/crypto/crypto` module:
//!
//! - **Input** `*mut HewVec` pointers are borrowed for the duration of the
//!   call.  Ownership stays with the Hew caller; the callee does NOT free them.
//!   `hwvec_to_u8` / `try_hwvec_to_u8` **copies** the payload bytes into a
//!   fresh `Vec<u8>` â€” it does not consume or free the `HewVec` header.
//! - **Output** `*mut HewVec` pointers are fresh allocations from
//!   `u8_to_hwvec`, which copies the Rust-side bytes into a new runtime-owned
//!   `HewVec`.  The Hew runtime's standard vec-drop path (`hew_vec_dealloc`)
//!   is the only correct way to release them.  No bespoke free entrypoint is
//!   introduced.
//! - The caller must never free an input `HewVec` before it is out of scope,
//!   nor alias an output `HewVec` pointer after passing it back to the runtime.
//! - `hew_ed25519_verify_hew` returns `i32` (0/1), not a `*mut HewVec`.
//!   Its ABI mirrors `hew_constant_time_eq_hew` in the crypto module.
//! - The raw C-ABI variants (`hew_ed25519_*` without `_hew` suffix) are
//!   provided for direct use from other native Rust code or future FFI callers;
//!   they do not touch `HewVec` at all.
//!
//! ## Hew surface mapping
//!
//! The Hew `keypair()` function calls two `_hew` functions in sequence:
//!
//! 1. `hew_ed25519_generate_pkcs8_hew()` â†’ `bytes` (83-byte PKCS#8 document)
//! 2. `hew_ed25519_public_key_from_pkcs8_hew(pkcs8)` â†’ `bytes` (32-byte public key)
//!
//! This keeps each FFI call to the `fn(args) -> bytes` shape already used by
//! `hew_sha256_hew` and friends â€” no novel calling convention is introduced.
//!
//! ## Fail-closed policy
//!
//! - The raw C-ABI variants (`hew_ed25519_*` without `_hew` suffix) treat
//!   null pointers and zero-length inputs as invalid and return `0`/empty
//!   sentinel rather than causing UB.
//! - The `_hew` `HewVec` wrappers use `try_hwvec_to_u8` to validate each
//!   `*mut HewVec` input (null check + shape check) **before** touching its
//!   contents.  On null or malformed `HewVec`:
//!   - `hew_ed25519_verify_hew` returns `0` (fail-closed sentinel).
//!   - `hew_ed25519_sign_hew` and `hew_ed25519_public_key_from_pkcs8_hew`
//!     call `hew_panic_msg` (controlled trap, same pattern as
//!     `hew_encrypt_seal_hew`) â€” the caller's contract is to pass valid
//!     `bytes` `HewVec` pointers.
//! - `hew_ed25519_sign_hew` also calls `hew_panic_msg` when the private key
//!   bytes are not a valid 83-byte PKCS#8 document.
//! - Secret-key bytes are never written to error messages or logs.

// Force-link hew-runtime so the linker can resolve hew_vec_* symbols
// referenced by hew-cabi's object code.
#[cfg(test)]
extern crate hew_runtime;

use ring::{
    rand::SystemRandom,
    signature::{Ed25519KeyPair, KeyPair, UnparsedPublicKey, ED25519},
};

/// Length (bytes) of an Ed25519 PKCS#8 v2 document as generated by ring 0.17.
///
/// Ring uses a PKCS#8 v2 template (`ed25519_pkcs8_v2_template.der`, 19 bytes)
/// plus the 32-byte private scalar plus the 32-byte public point = 83 bytes.
/// This constant is asserted at runtime in debug builds; if ring's encoding
/// changes a `debug_assert!` in `hew_ed25519_generate_pkcs8` will catch it.
pub const ED25519_PKCS8_LEN: usize = 83;

/// Length (bytes) of a raw Ed25519 public key.
pub const ED25519_PUBLIC_LEN: usize = 32;

/// Length (bytes) of an Ed25519 signature.
pub const ED25519_SIG_LEN: usize = 64;

// ---------------------------------------------------------------------------
// Raw C-ABI layer (no HewVec; usable from native callers)
// ---------------------------------------------------------------------------

/// Generate an Ed25519 PKCS#8 v2 document and write the 83 bytes to `out`.
///
/// Returns `1` on success, `0` if the RNG failed or `out` is null.
///
/// # Safety
///
/// `out` must be valid for writing `ED25519_PKCS8_LEN` (83) bytes.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_generate_pkcs8(out: *mut u8) -> i32 {
    if out.is_null() {
        return 0;
    }
    let rng = SystemRandom::new();
    let Ok(pkcs8_doc) = Ed25519KeyPair::generate_pkcs8(&rng) else {
        return 0;
    };
    let bytes = pkcs8_doc.as_ref();
    debug_assert_eq!(
        bytes.len(),
        ED25519_PKCS8_LEN,
        "ring Ed25519 PKCS#8 document length changed"
    );
    // SAFETY: out is valid for ED25519_PKCS8_LEN bytes per caller contract.
    unsafe { std::ptr::copy_nonoverlapping(bytes.as_ptr(), out, bytes.len()) };
    1
}

/// Extract the 32-byte public key from an Ed25519 PKCS#8 document.
///
/// Returns `1` on success, `0` if either pointer is null or the key is invalid.
///
/// # Safety
///
/// - `pkcs8` must be valid for reading `pkcs8_len` bytes.
/// - `out` must be valid for writing `ED25519_PUBLIC_LEN` (32) bytes.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_public_key_from_pkcs8(
    pkcs8: *const u8,
    pkcs8_len: usize,
    out: *mut u8,
) -> i32 {
    if pkcs8.is_null() || out.is_null() {
        return 0;
    }
    // SAFETY: pkcs8 is valid for pkcs8_len bytes per caller contract.
    let key_bytes = unsafe { std::slice::from_raw_parts(pkcs8, pkcs8_len) };
    let Ok(key_pair) = Ed25519KeyPair::from_pkcs8(key_bytes) else {
        return 0;
    };
    let pub_bytes = key_pair.public_key().as_ref();
    debug_assert_eq!(pub_bytes.len(), ED25519_PUBLIC_LEN);
    // SAFETY: out is valid for ED25519_PUBLIC_LEN bytes per caller contract.
    unsafe { std::ptr::copy_nonoverlapping(pub_bytes.as_ptr(), out, pub_bytes.len()) };
    1
}

/// Sign `msg` (`msg_len` bytes) with the PKCS#8 private key (`key`, `key_len`
/// bytes) and write the 64-byte signature into `sig_out`.
///
/// Returns `1` on success, `0` if any input is null/invalid.
///
/// # Safety
///
/// - `key` must be valid for reading `key_len` bytes.
/// - `msg` must be valid for reading `msg_len` bytes (may be null when
///   `msg_len == 0`).
/// - `sig_out` must be valid for writing `ED25519_SIG_LEN` (64) bytes.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_sign(
    key: *const u8,
    key_len: usize,
    msg: *const u8,
    msg_len: usize,
    sig_out: *mut u8,
) -> i32 {
    if key.is_null() || sig_out.is_null() {
        return 0;
    }
    // SAFETY: key is valid for key_len bytes per caller contract.
    let key_bytes = unsafe { std::slice::from_raw_parts(key, key_len) };
    // SAFETY: msg is valid for msg_len bytes, or empty when msg_len == 0.
    let msg_bytes: &[u8] = if msg.is_null() || msg_len == 0 {
        &[]
    } else {
        // SAFETY: msg is non-null and valid for msg_len bytes.
        unsafe { std::slice::from_raw_parts(msg, msg_len) }
    };

    // Invalid key: fail closed.  Do NOT write anything to sig_out.
    let Ok(key_pair) = Ed25519KeyPair::from_pkcs8(key_bytes) else {
        return 0;
    };

    let sig = key_pair.sign(msg_bytes);
    let sig_bytes = sig.as_ref();
    debug_assert_eq!(sig_bytes.len(), ED25519_SIG_LEN);
    // SAFETY: sig_out is valid for ED25519_SIG_LEN bytes per caller contract.
    unsafe { std::ptr::copy_nonoverlapping(sig_bytes.as_ptr(), sig_out, sig_bytes.len()) };
    1
}

/// Verify that `sig` (`sig_len` bytes) is a valid Ed25519 signature over `msg`
/// (`msg_len` bytes) under `public_key` (`key_len` bytes).
///
/// Returns `1` if valid, `0` otherwise (including any parse or length error).
///
/// # Safety
///
/// - `public_key` must be valid for reading `key_len` bytes.
/// - `msg` must be valid for reading `msg_len` bytes (may be null when
///   `msg_len == 0`).
/// - `sig` must be valid for reading `sig_len` bytes.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_verify(
    public_key: *const u8,
    key_len: usize,
    msg: *const u8,
    msg_len: usize,
    sig: *const u8,
    sig_len: usize,
) -> i32 {
    if public_key.is_null() || sig.is_null() {
        return 0;
    }
    // SAFETY: public_key is valid for key_len bytes per caller contract.
    let key_bytes = unsafe { std::slice::from_raw_parts(public_key, key_len) };
    // SAFETY: msg is valid for msg_len bytes, or empty when msg_len == 0.
    let msg_bytes: &[u8] = if msg.is_null() || msg_len == 0 {
        &[]
    } else {
        // SAFETY: msg is non-null and valid for msg_len bytes.
        unsafe { std::slice::from_raw_parts(msg, msg_len) }
    };
    // SAFETY: sig is valid for sig_len bytes per caller contract.
    let sig_bytes = unsafe { std::slice::from_raw_parts(sig, sig_len) };

    let unparsed = UnparsedPublicKey::new(&ED25519, key_bytes);
    match unparsed.verify(msg_bytes, sig_bytes) {
        Ok(()) => 1,
        Err(_) => 0,
    }
}

