/// Generate an Ed25519 PKCS#8 v2 document and write the 83 bytes to `out`.
///
/// Returns `1` on success, `0` if the RNG failed or `out` is null.
///
/// # Safety
///
/// `out` must be valid for writing `ED25519_PKCS8_LEN` (83) bytes.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_generate_pkcs8(out: *mut u8) -> i32 {
    if out.is_null() {
        return 0;
    }
    let rng = SystemRandom::new();
    let Ok(pkcs8_doc) = Ed25519KeyPair::generate_pkcs8(&rng) else {
        return 0;
    };
    let bytes = pkcs8_doc.as_ref();
    // Fail closed via the checked seam: copy only if ring's PKCS#8 document is
    // exactly ED25519_PKCS8_LEN bytes, never overflowing `out` if the encoded
    // length ever changes.  Returns 0 without writing on a mismatch.
    // SAFETY: out is valid for ED25519_PKCS8_LEN bytes per caller contract.
    unsafe { fill_fixed::<ED25519_PKCS8_LEN>(out, bytes) }
}

/// Extract the 32-byte public key from an Ed25519 PKCS#8 document.
///
/// Returns `1` on success, `0` if either pointer is null or the key is invalid.
///
/// # Safety
///
/// - `pkcs8` must be valid for reading `pkcs8_len` bytes.
/// - `out` must be valid for writing `ED25519_PUBLIC_LEN` (32) bytes.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_public_key_from_pkcs8(
    pkcs8: *const u8,
    pkcs8_len: usize,
    out: *mut u8,
) -> i32 {
    if pkcs8.is_null() || out.is_null() {
        return 0;
    }
    // SAFETY: pkcs8 is valid for pkcs8_len bytes per caller contract.
    let key_bytes = unsafe { std::slice::from_raw_parts(pkcs8, pkcs8_len) };
    let Ok(key_pair) = Ed25519KeyPair::from_pkcs8(key_bytes) else {
        return 0;
    };
    let pub_bytes = key_pair.public_key().as_ref();
    // Fail closed via the checked seam: copy only if the public key is exactly
    // ED25519_PUBLIC_LEN bytes, preventing an overflow if the length ever
    // diverges from 32.  Returns 0 without writing on a mismatch.
    // SAFETY: out is valid for ED25519_PUBLIC_LEN bytes per caller contract.
    unsafe { fill_fixed::<ED25519_PUBLIC_LEN>(out, pub_bytes) }
}

/// Sign `msg` (`msg_len` bytes) with the PKCS#8 private key (`key`, `key_len`
/// bytes) and write the 64-byte signature into `sig_out`.
///
/// Returns `1` on success, `0` if any input is null/invalid.
///
/// # Safety
///
/// - `key` must be valid for reading `key_len` bytes.
/// - `msg` must be valid for reading `msg_len` bytes (may be null when
///   `msg_len == 0`).
/// - `sig_out` must be valid for writing `ED25519_SIG_LEN` (64) bytes.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_sign(
    key: *const u8,
    key_len: usize,
    msg: *const u8,
    msg_len: usize,
    sig_out: *mut u8,
) -> i32 {
    if key.is_null() || sig_out.is_null() {
        return 0;
    }
    // SAFETY: key is valid for key_len bytes per caller contract.
    let key_bytes = unsafe { std::slice::from_raw_parts(key, key_len) };
    // SAFETY: msg is valid for msg_len bytes, or empty when msg_len == 0.
    let msg_bytes: &[u8] = if msg.is_null() || msg_len == 0 {
        &[]
    } else {
        // SAFETY: msg is non-null and valid for msg_len bytes.
        unsafe { std::slice::from_raw_parts(msg, msg_len) }
    };

    // Invalid key: fail closed.  Do NOT write anything to sig_out.
    let Ok(key_pair) = Ed25519KeyPair::from_pkcs8(key_bytes) else {
        return 0;
    };

    let sig = key_pair.sign(msg_bytes);
    let sig_bytes = sig.as_ref();
    // Fail closed via the checked seam: copy only if the signature is exactly
    // ED25519_SIG_LEN bytes, preventing an overflow if the length ever diverges
    // from 64.  Returns 0 without writing on a mismatch.
    // SAFETY: sig_out is valid for ED25519_SIG_LEN bytes per caller contract.
    unsafe { fill_fixed::<ED25519_SIG_LEN>(sig_out, sig_bytes) }
}

/// Verify that `sig` (`sig_len` bytes) is a valid Ed25519 signature over `msg`
/// (`msg_len` bytes) under `public_key` (`key_len` bytes).
///
/// Returns `1` if valid, `0` otherwise (including any parse or length error).
///
/// # Safety
///
/// - `public_key` must be valid for reading `key_len` bytes.
/// - `msg` must be valid for reading `msg_len` bytes (may be null when
///   `msg_len == 0`).
/// - `sig` must be valid for reading `sig_len` bytes.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_verify(
    public_key: *const u8,
    key_len: usize,
    msg: *const u8,
    msg_len: usize,
    sig: *const u8,
    sig_len: usize,
) -> i32 {
    if public_key.is_null() || sig.is_null() {
        return 0;
    }
    // SAFETY: public_key is valid for key_len bytes per caller contract.
    let key_bytes = unsafe { std::slice::from_raw_parts(public_key, key_len) };
    // SAFETY: msg is valid for msg_len bytes, or empty when msg_len == 0.
    let msg_bytes: &[u8] = if msg.is_null() || msg_len == 0 {
        &[]
    } else {
        // SAFETY: msg is non-null and valid for msg_len bytes.
        unsafe { std::slice::from_raw_parts(msg, msg_len) }
    };
    // SAFETY: sig is valid for sig_len bytes per caller contract.
    let sig_bytes = unsafe { std::slice::from_raw_parts(sig, sig_len) };

    let unparsed = UnparsedPublicKey::new(&ED25519, key_bytes);
    match unparsed.verify(msg_bytes, sig_bytes) {
        Ok(()) => 1,
        Err(_) => 0,
    }
}

