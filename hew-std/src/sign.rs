//   - `bytes` inputs: `*const BytesTriple` (by-pointer consumer convention).
//     Codegen passes the address of the caller's BytesTriple alloca.
//     `is_bytes_by_pointer_consumer` in `hew-codegen-rs/src/llvm.rs` must
//     list every function that receives bytes this way.
//   - `bytes` outputs: `BytesTriple` by value.  Codegen uses the `_raw`
//     out-pointer sibling on all platforms to avoid the Windows x64 MSVC sret
//     mismatch.  `is_bytes_triple_return_producer` in codegen must list every
//     function that returns bytes this way.

/// Generate an Ed25519 PKCS#8 v2 document and return it as an 83-byte
/// `bytes` value (`BytesTriple`).
///
/// Calls `abort()` on RNG failure (should not occur in practice).
///
/// # Safety
///
/// No inputs; all memory is managed by the runtime allocator.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_generate_pkcs8_hew() -> BytesTriple {
    let mut buf = [0u8; ED25519_PKCS8_LEN];
    // SAFETY: buf is a valid ED25519_PKCS8_LEN-byte writable buffer.
    let ok = unsafe { hew_ed25519_generate_pkcs8(buf.as_mut_ptr()) };
    if ok == 0 {
        abort_sign_failure();
    }
    bytes_from_slice(&buf)
}

/// Out-pointer variant of [`hew_ed25519_generate_pkcs8_hew`] for Windows x64
/// MSVC sret compatibility.
///
/// Writes the result into `*out` and returns void.  Every bytes-triple producer
/// exports a `_raw` sibling so that codegen can use the out-pointer calling
/// convention on all platforms.
///
/// # Safety
///
/// `out` must be a valid, writable pointer to a `BytesTriple` slot.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_generate_pkcs8_hew_raw(out: *mut BytesTriple) {
    // SAFETY: preconditions forwarded from caller contract above.
    let triple = unsafe { hew_ed25519_generate_pkcs8_hew() };
    // SAFETY: caller guarantees `out` is a valid BytesTriple slot.
    unsafe { out.write(triple) };
}

/// Extract the 32-byte public key from an Ed25519 PKCS#8 `bytes` value and
/// return it as a new 32-byte `bytes` value (`BytesTriple`).
///
/// Calls `hew_panic_msg` if `pkcs8` is null or is not a valid 83-byte PKCS#8
/// document.
///
/// # Safety
///
/// `pkcs8` must be a valid, non-null pointer to a `BytesTriple` whose payload
/// is an 83-byte Ed25519 PKCS#8 v2 document.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_public_key_from_pkcs8_hew(
    pkcs8: *const BytesTriple,
) -> BytesTriple {
    // SAFETY: caller guarantees pkcs8 is non-null and valid.
    let Some(pkcs8_bytes) = (unsafe { bytes_from_triple(pkcs8) }) else {
        panic_msg(b"sign.keypair failed: pkcs8 must be a valid non-null bytes value\0")
    };
    let mut pub_buf = [0u8; ED25519_PUBLIC_LEN];
    // SAFETY: pkcs8_bytes and pub_buf are valid buffers.
    let ok = unsafe {
        hew_ed25519_public_key_from_pkcs8(
            pkcs8_bytes.as_ptr(),
            pkcs8_bytes.len(),
            pub_buf.as_mut_ptr(),
        )
    };
    if ok == 0 {
        panic_msg(b"sign.keypair failed: private_key must be an 83-byte Ed25519 PKCS#8 document\0");
    }
    bytes_from_slice(&pub_buf)
}

/// Out-pointer variant of [`hew_ed25519_public_key_from_pkcs8_hew`] for Windows
/// x64 MSVC sret compatibility.
///
/// # Safety
///
/// - `pkcs8` must be a valid, non-null pointer to a `BytesTriple`.
/// - `out` must be a valid, writable pointer to a `BytesTriple` slot.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_public_key_from_pkcs8_hew_raw(
    pkcs8: *const BytesTriple,
    out: *mut BytesTriple,
) {
    // SAFETY: preconditions forwarded from caller contract above.
    let triple = unsafe { hew_ed25519_public_key_from_pkcs8_hew(pkcs8) };
    // SAFETY: caller guarantees `out` is a valid BytesTriple slot.
    unsafe { out.write(triple) };
}

/// Sign `message` (a `bytes` value) with `private_key` (an 83-byte PKCS#8
/// `bytes` value) and return the 64-byte signature as a `bytes` value
/// (`BytesTriple`).
///
/// Calls `hew_panic_msg` if either input is null or `private_key` is not a
/// valid 83-byte PKCS#8 document.
///
/// # Safety
///
/// - `message` must be a valid, non-null pointer to a `BytesTriple` (may be
///   zero-length).
/// - `private_key` must be a valid, non-null pointer to a `BytesTriple`.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_sign_hew(
    message: *const BytesTriple,
    private_key: *const BytesTriple,
) -> BytesTriple {
    // SAFETY: caller guarantees private_key is non-null and valid.
    let Some(key_bytes) = (unsafe { bytes_from_triple(private_key) }) else {
        panic_msg(b"sign.sign failed: private_key must be a valid non-null bytes value\0")
    };
    // SAFETY: caller guarantees message is non-null and valid.
    let msg_bytes = unsafe { bytes_from_triple(message) }.unwrap_or_default();

    let mut sig_buf = [0u8; ED25519_SIG_LEN];
    // SAFETY: key_bytes and msg_bytes are valid slices; sig_buf is a 64-byte buffer.
    let ok = unsafe {
        hew_ed25519_sign(
            key_bytes.as_ptr(),
            key_bytes.len(),
            msg_bytes.as_ptr(),
            msg_bytes.len(),
            sig_buf.as_mut_ptr(),
        )
    };
    if ok == 0 {
        panic_msg(b"sign.sign failed: private_key must be an 83-byte Ed25519 PKCS#8 document\0");
    }
    bytes_from_slice(&sig_buf)
}

/// Out-pointer variant of [`hew_ed25519_sign_hew`] for Windows x64 MSVC sret
/// compatibility.
///
/// # Safety
///
/// - `message` must be a valid, non-null pointer to a `BytesTriple`.
/// - `private_key` must be a valid, non-null pointer to a `BytesTriple`.
/// - `out` must be a valid, writable pointer to a `BytesTriple` slot.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_sign_hew_raw(
    message: *const BytesTriple,
    private_key: *const BytesTriple,
    out: *mut BytesTriple,
) {
    // SAFETY: preconditions forwarded from caller contract above.
    let triple = unsafe { hew_ed25519_sign_hew(message, private_key) };
    // SAFETY: caller guarantees `out` is a valid BytesTriple slot.
    unsafe { out.write(triple) };
}

/// Verify an Ed25519 `signature` over `message` using `public_key`.
///
/// Returns `1` if the signature is valid, `0` otherwise.  Never panics or
/// aborts â€” null pointer inputs and verification failures all return a clean `0`.
///
/// # Safety
///
/// - `message`, `signature`, and `public_key` must each be either null or a
///   valid pointer to a live `BytesTriple`.  Dangling non-null pointers are
///   undefined behaviour.
#[no_mangle]
pub unsafe extern "C" fn hew_ed25519_verify_hew(
    message: *const BytesTriple,
    signature: *const BytesTriple,
    public_key: *const BytesTriple,
) -> i32 {
    // Treat null inputs as empty/missing â†’ return 0 (fail-closed).
    // SAFETY: each pointer is either null or a valid BytesTriple per caller contract.
    let Some(key_bytes) = (unsafe { bytes_from_triple(public_key) }) else {
        return 0;
    };
    // SAFETY: pointer is either null or a valid BytesTriple per caller contract.
    let msg_bytes = unsafe { bytes_from_triple(message) }.unwrap_or_default();
    // SAFETY: pointer is either null or a valid BytesTriple per caller contract.
    let Some(sig_bytes) = (unsafe { bytes_from_triple(signature) }) else {
        return 0;
    };

    // SAFETY: all slices are valid; raw verify handles length mismatches cleanly.
    unsafe {
        hew_ed25519_verify(
            key_bytes.as_ptr(),
            key_bytes.len(),
            msg_bytes.as_ptr(),
            msg_bytes.len(),
            sig_bytes.as_ptr(),
            sig_bytes.len(),
        )
    }
}

